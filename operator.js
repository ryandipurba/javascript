function panggilOperator() {
    var a = 20;
    var b = 10;

    console.log("20 + 10 = " + (a + b)); //penjumlahan
    console.log("20 - 10 = " + (a - b)); //pengurangan
    console.log("20 * 10 = " + (a * b)); //perkalian
    console.log("20 / 10 = " + (a / b)); //pembagian
    console.log("20 % 10 = " + (a % b)); //modulo
}

panggilOperator();